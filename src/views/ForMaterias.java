    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        // TODO add your handling code here:
        int idMateria=0;
        String nombre=jtfNombre.getText();
        int anio=0;
        
        try{
            idMateria=Integer.parseInt(jtfCodigo.getText());
            anio=Integer.parseInt(jtfAnio.getText());
            
        }catch(NumberFormatException e){
            
             JOptionPane.showMessageDialog(this,"Codigo erroneo");
            return;
        }
        Materia materia = new Materia(idMateria,nombre,anio);
        
         if(colegio.agregarMateria(materia)){
            JOptionPane.showMessageDialog(this, "Materia agregada exitosamente");
        }else{
            JOptionPane.showMessageDialog(this, "Ya se ha registrado esa materia");
        }
    }//GEN-LAST:event_jbGuardarActionPerformed

